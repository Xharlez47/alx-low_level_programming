/**
 * _islower - Entry point
 *
 * Description: Using function and nested loops for a program
 * that "Returns 1 if c is lowercase Returns 0 otherwise"
 * Return: 1 'c' (if true) or 0 (if false)
 */
int _islower(int c)
{
	int wa = 0;
	int c = 1;
	int ya;
	int za;

	for (ya = 'A'; ya <= 'Z'; ya++)
{
		_putchar(wa);
	for (za = 'a'; za <= 'z'; za++)
		_putchar(c);
}
return (1);
}
