/**
 * _abs - Entry point
 *
 * Description: Using function and nested loops for a program
 * that "computes the absolute value of an integer"
 * @a: is an integer
 * Return: int
 */
int _abs(int a)
{

if (a > 0)
{
	return (a);
}
else if (a == 0)
{
	return (a);
}
else
{
	return (-a);
}
}
